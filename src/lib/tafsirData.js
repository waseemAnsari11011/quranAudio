/**
 * Get tafsir audio URLs for a specific chapter
 * @param {number} surahId - The chapter/surah number
 * @returns {Array<string>} - Array of tafsir audio URLs
 */
export const getTafsirForChapter = (surahId) => {
  const chapterStr = String(surahId).padStart(3, "0");

  // Find all tafsir entries for this chapter (including parts like 002.1, 002.2)
  const chapterTafsirs = tafsirData.filter((item) => {
    // Handle both exact matches (e.g., "001") and parts (e.g., "002.1", "002.2")
    return item.ch === chapterStr || item.ch.startsWith(chapterStr + ".");
  });

  // Extract and validate URLs
  return chapterTafsirs
    .map((item) => item.link)
    .filter((link) => link && link.startsWith("https://")) // Filter out invalid URLs
    .map((link) => link.replace("httpss://", "https://")); // Fix any typos like "httpss"
};
